Cypress.Commands.add('clickButton', (buttonName) => {
    cy.get(`button`)
    .filter((index, el) => {
      return Cypress.$(el).text().trim() === buttonName;
    }).first().should(($element) => {
        expect($element,`Le bouton "${buttonName}"`).to.exist;
      }).click({force:true,multiple:true});
  });